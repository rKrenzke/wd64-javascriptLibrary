// ASSIGNMENT OPERATOR:
let r = 5;
// our equal sign is the assignment.

//ADDITION
r += 1; // r = r + 1 or, 5 + 1
console.log(r);

//SUBTRACTION
r -= 2;  // r = r + 2
console.log(r);

//MULTIPLICATION
r *= 2;
console.log(r);

//DIVISION
r /= 1;
console.log(r);

//EXPONENTIAL
r **= 2;
console.log(r);

//REMAINDER
r %= 1;
console.log(r);